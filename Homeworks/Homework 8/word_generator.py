def word_gen(s: str) -> str:
    """
    Генератор слів, який приймає рядок слів та повертає кожне слово за допомогою оператора yield.
    :param s: Рядок слів, який містить слова, розділені пробілами.
    :yield: Послідовність слів, що містяться у рядку.
    """
    words = s.split()  # розділяємо рядок на слова за пробілами
    for word in words:
        yield word  # повертаємо слово як результат генератора
