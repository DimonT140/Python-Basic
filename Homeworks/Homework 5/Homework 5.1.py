# просимо ввести стрічку та переводимоїї в нижній регістр
string = input("Введіть стрічку: ").lower()

# Видаляємо все зайве зі стрічки
string = string.translate(str.maketrans('', '', ' \n\t!@#$%^&*()_-+={}[]|:;"<>,.?/~`'))

# Перевіряємо, чи є введена стрічка паліндромом і виводимо результат
if string == string[::-1]:
    print('Це паліндром!')
else:
    print('Це не паліндром!')
