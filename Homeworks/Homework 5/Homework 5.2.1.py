# просимо користувача ввести текст з дужками
text = input("Введіть текст з дужками: ")
# поки в тексті є символ '(', виконуємо цикл
while '(' in text:
    # знаходимо першу ліву дужку
    start = text.find('(')
    # знаходимо праву дужку, починаючи з лівої дужки
    end = text.find(')', start)
    # якщо права дужка знайдена, видаляємо текст між дужками
    if end != -1:
        text = text[:start] + text[end+1:]
    # якщо права дужка не знайдена, виходимо з циклу
    else:
        break
# замінюємо у тексті всі символи '(' та ')' на порожній рядок
text = text.replace('(', '')
text = text.replace(')', '')
# виводимо результат
print(text)
